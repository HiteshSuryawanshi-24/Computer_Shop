    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            ResultSet myrs;
            DB.opencon();
            
            myrs = DB.select(" select * from compshopitem where itemid= '"+TextItemId.getText().toString()+"' ");
            myrs.next();
            cmbBrand.setSelectedItem(myrs.getString("brand"));
            TextName.setText(myrs.getString("name"));
            TextPrice.setText(myrs.getString("price"));
            TextStock.setText(myrs.getString("stock"));
            
            Blob b = myrs.getBlob("image");             //for converting binary to image
            Image myimg = new ImageIcon(b.getBytes(1, (int) b.length())).getImage().getScaledInstance(lblImage.getWidth(), lblImage.getHeight(),Image.SCALE_SMOOTH);
            Icon myicon = new ImageIcon(myimg);
            lblImage.setIcon(myicon);
            
            DB.closeCon();
            
            
        }
        catch (SQLException ex) 
        {
            Logger.getLogger(ItemManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

