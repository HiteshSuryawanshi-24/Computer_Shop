    private void lblImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImageMouseClicked
        // TODO add your handling code here:
        
         //create file
        JFileChooser jfc = new JFileChooser(); //create JFilsChooser Obj
        
        FileNameExtensionFilter fnm = new FileNameExtensionFilter("*.images","jpng","png","gif"); //to change file to image extensionn
        jfc.addChoosableFileFilter(fnm);
        
        int result = jfc.showOpenDialog(this);
        if(result == JFileChooser.APPROVE_OPTION) //when you click on OK button then this operation performed
        {
            System.out.println("File Selected...");
            myfile = jfc.getSelectedFile(); // for current selected file
            
            System.out.println("PATH :  "+ myfile.getPath()); // for displaying file Location(PATH)
            
            Image myimage = new ImageIcon(myfile.getAbsolutePath()).getImage().getScaledInstance(lblImage.getWidth(),lblImage.getHeight(),Image.SCALE_SMOOTH);//to set image into label size to fit           
            Icon myicon = new ImageIcon(myimage); // convertion image to icon
            lblImage.setIcon(myicon);
            
        }
        else
        {
            System.out.println("FIle NOT Selected..");
            
        }
        
        
        
    }//GEN-LAST:event_lblImageMouseClicked

