    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            // TODO add your handling code here:
            DB.opencon();
            
            PreparedStatement pstmt = DBConn.con.prepareStatement("insert into compshopitem values(?,?,?,?,?,?,?,?)");
            pstmt.setString(1, TextName.getText().toString());
            pstmt.setString(2,cmbCategory.getSelectedItem().toString());
            pstmt.setString(3, cmbUnit.getSelectedItem().toString());
            pstmt.setString(4,TextItemId.getText().toString());
            pstmt.setString(5,cmbBrand.getSelectedItem().toString());
            pstmt.setString(6, TextStock.getText().toString());
            pstmt.setString(7, TextPrice.getText().toString());
            
            FileInputStream fin = new FileInputStream(myfile.getAbsolutePath());   //for converting image into binary
            pstmt.setBinaryStream(8, fin, fin.available());
            pstmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Item Data Added Successfully..");
            
            //TextName.requestFocus();
            DB.closeCon();
                        
            
            
        } catch (SQLException ex) 
        {
            Logger.getLogger(ItemManagement.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ItemManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

